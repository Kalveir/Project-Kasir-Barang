    static int hitungTotal(int total, int total_harga){
        int Jumlah_Harga = total + total_harga;
        return Jumlah_Harga;
    }
    static int Jumlah_Cashback(int total_harga, int Transaksi_uang ){
        return Transaksi_uang - total_harga;
    }
    static String moneyFormat(int nomer){
        Locale indo = new Locale("in", "ID");
        Currency rupiah = Currency.getInstance(indo);
        NumberFormat rupiahFormat = NumberFormat.getCurrencyInstance(indo);
        return rupiahFormat.format(nomer);
}

