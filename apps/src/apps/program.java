    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        try{
                //        mengambil data
            String Barang = (String) PilihBarang.getSelectedItem();
            int harga = Integer.parseInt(HargaBarang.getText());
            int jumlah = Integer.parseInt(JumlahBarang.getText());
            int total_harga = Integer.parseInt(TotalHarga.getText());
            int Transaksi_uang = Integer.parseInt(Transaksi.getText());
            //total harga
            int total = harga * jumlah;
            int Hitung_Total =hitungTotal(total,total_harga);
            TotalHarga.setText(String.valueOf(Hitung_Total));
            //format Rupiah
            String hrg_brg = moneyFormat(harga);
            String ttl_hrg = moneyFormat(total);
            //input data ke tabel
            tbl.addRow(new Object[]{
                Barang, jumlah, hrg_brg, ttl_hrg
            });
            Tabel_Data.setModel(tbl);
            JumlahBarang.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Data Kosong");
        }
        
        
    }//GEN-LAST:event_AddButtonActionPerformed

